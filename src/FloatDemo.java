public class FloatDemo {
    public static void main(String[] args) {
        // 声明double类型变量，可以在结尾加D或者d，也可以不加
        double price = 9.5;
        System.out.println(price);
        // float类型在末尾必须用F或者f结尾
        float height = 169.5F;
        System.out.println(height);
        System.out.println(0.1 + 0.2);
    }
}
